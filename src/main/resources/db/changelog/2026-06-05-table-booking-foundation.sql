-- Foundation for table reservations.
-- Event bookings stay separate: they describe large/private events, while
-- table reservations describe concrete seating and time windows.

CREATE TABLE IF NOT EXISTS venue_tables (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    venue_code VARCHAR(64) NOT NULL DEFAULT 'AERIS',
    table_code VARCHAR(32) NOT NULL,
    display_name VARCHAR(120) NOT NULL,
    zone VARCHAR(40) NOT NULL,
    capacity_min INTEGER NOT NULL DEFAULT 1,
    capacity_max INTEGER NOT NULL,
    combinable_group VARCHAR(64),
    bookable BOOLEAN NOT NULL DEFAULT TRUE,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    plan_page INTEGER NOT NULL DEFAULT 2,
    plan_ref TEXT,
    sort_order INTEGER NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uq_venue_tables_code UNIQUE (venue_code, table_code),
    CONSTRAINT chk_venue_tables_capacity CHECK (capacity_min > 0 AND capacity_max >= capacity_min)
);

CREATE INDEX IF NOT EXISTS idx_venue_tables_zone_active
    ON venue_tables(venue_code, zone, active, sort_order);

