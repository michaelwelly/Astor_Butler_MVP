CREATE TABLE IF NOT EXISTS table_reservation_orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    chat_id BIGINT NOT NULL,
    telegram_user_id BIGINT,
    user_id BIGINT,
    table_id BIGINT,
    status VARCHAR(40) NOT NULL DEFAULT 'DRAFT',
    source VARCHAR(40) NOT NULL DEFAULT 'TELEGRAM',
    requested_start_at TIMESTAMPTZ,
    requested_end_at TIMESTAMPTZ,
    party_size INTEGER,
    guest_name VARCHAR(160),
    guest_phone VARCHAR(80),
    guest_comment TEXT,
    manager_telegram_id BIGINT NOT NULL DEFAULT 876857557,
    manager_user_id BIGINT,
    hostess_chat_id VARCHAR(120),
    sbis_external_id VARCHAR(120),
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_table_reservation_party_size CHECK (party_size IS NULL OR party_size > 0),
    CONSTRAINT chk_table_reservation_window CHECK (
        requested_start_at IS NULL
        OR requested_end_at IS NULL
        OR requested_end_at > requested_start_at
    ),
    CONSTRAINT fk_table_reservation_orders_table
        FOREIGN KEY (table_id) REFERENCES venue_tables(id)
        ON DELETE SET NULL,
    CONSTRAINT fk_table_reservation_orders_user
        FOREIGN KEY (user_id) REFERENCES users(id)
        ON DELETE SET NULL,
    CONSTRAINT fk_table_reservation_orders_manager_user
        FOREIGN KEY (manager_user_id) REFERENCES users(id)
        ON DELETE SET NULL
);

CREATE INDEX IF NOT EXISTS idx_table_reservation_orders_chat_created
    ON table_reservation_orders(chat_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_table_reservation_orders_status_time
    ON table_reservation_orders(status, requested_start_at);

CREATE INDEX IF NOT EXISTS idx_table_reservation_orders_table_time
    ON table_reservation_orders(table_id, requested_start_at, requested_end_at)
    WHERE table_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS table_reservation_holds (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_id BIGINT NOT NULL,
    order_id BIGINT NOT NULL,
    status VARCHAR(40) NOT NULL DEFAULT 'HELD',
    start_at TIMESTAMPTZ NOT NULL,
    end_at TIMESTAMPTZ NOT NULL,
    expires_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_table_reservation_holds_window CHECK (end_at > start_at),
    CONSTRAINT fk_table_reservation_holds_table
        FOREIGN KEY (table_id) REFERENCES venue_tables(id)
        ON DELETE CASCADE,
    CONSTRAINT fk_table_reservation_holds_order
        FOREIGN KEY (order_id) REFERENCES table_reservation_orders(id)
        ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_table_reservation_holds_table_window
    ON table_reservation_holds(table_id, start_at, end_at, status);

CREATE INDEX IF NOT EXISTS idx_table_reservation_holds_order
    ON table_reservation_holds(order_id);
